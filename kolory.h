#ifndef KOLORY_H
#define KOLORY_H

/*Kolory uzyte do wyswietlenia kolorowego tekstu w terminalu*/
#define RED	"\033[31m"		/*Czerwony*/
#define GREEN "\033[32m"	/*Zielony*/
#define YELLOW "\033[33m"	/*Zolty*/
#define BLUE "\033[34m"		/*Niebieski*/
#define MAGENTA "\033[35m"	/*Magenta*/
#define CYAN "\033[36m"		/*Cyjan*/

#define BRIGHT_BLACK "\033[90m"	/*Szary*/
#define BRIGHT_CYAN "\033[96m"	/*Jasny cyjan*/
#define BRIGHT_RED "\033[91m"	/*Jasnoczerwony*/

#define BRIGHT_RED_BG "\033[101m" /*Jasnoczerwone tlo*/

#define RESET "\033[0m" /*Reset koloru do domyslnego*/

#endif